const fuse = new Fuse(customers, {
  includeScore: true,
  threshold: 0.4
});

export default function App() {
  const [customerInput, setCustomerInput] = useState('');
  const [filteredCustomers, setFilteredCustomers] = useState([]);
  const [question2, setQuestion2] = useState('');
  const [question3, setQuestion3] = useState('');

  const handleCustomerChange = (e) => {
    const input = e.target.value;
    setCustomerInput(input);
    if (input.trim() === '') {
      setFilteredCustomers([]);
      return;
    }
    const results = fuse.search(input).map(result => result.item);
    setFilteredCustomers(results);
  };

  const handleSelectCustomer = (name) => {
    setCustomerInput(name);
    setFilteredCustomers([]);
  };

  const handleSubmit = async (e) => {
    e.preventDefault();

    const payload = {
      customer: customerInput,
      question2,
      question3
    };

    try {
      await fetch('https://script.google.com/macros/s/AKfycbySwFJqPK6SMoHKs0w544RSgxhE2-cBV-IB-O_JCOerB8jn1SUWYkeCwRxmexCz441nEw/exec', {
        method: 'POST',
        mode: 'no-cors', // no-cors mode disables reading response
        headers: {
          'Content-Type': 'application/json'
        },
        body: JSON.stringify(payload)
      });

      alert('Form submitted successfully!');
      setCustomerInput('');
      setQuestion2('');
      setQuestion3('');
    } catch (error) {
      alert('Error submitting form.');
      console.error(error);
    }
  };

  return (
    <div style={{ maxWidth: 600, margin: '40px auto', padding: 20 }}>
      <h1 style={{ textAlign: 'center' }}>Customer Form</h1>

      <form onSubmit={handleSubmit}>
        <div style={{ marginBottom: 20 }}>
          <label>Customer Name and code</label>
          <input
            type="text"
            value={customerInput}
            onChange={handleCustomerChange}
            placeholder="Type or select a customer"
            style={{ width: '100%', padding: 8 }}
          />
          {filteredCustomers.length > 0 && (
            <ul style={{ border: '1px solid #ccc', marginTop: 4, listStyle: 'none', padding: 0 }}>
              {filteredCustomers.map((name, index) => (
                <li
                  key={index}
                  style={{ padding: 8, cursor: 'pointer' }}
                  onClick={() => handleSelectCustomer(name)}
                >
                  {name}
                </li>
              ))}
            </ul>
          )}
        </div>

        <div style={{ marginBottom: 20 }}>
          <label>กำหนดการวางบิล</label>
          <input
            type="text"
            value={question2}
            onChange={(e) => setQuestion2(e.target.value)}
            style={{ width: '100%', padding: 8 }}
          />
        </div>

        <div style={{ marginBottom: 20 }}>
          <label>กำหนดการรับเงิน</label>
          <input
            type="text"
            value={question3}
            onChange={(e) => setQuestion3(e.target.value)}
            style={{ width: '100%', padding: 8 }}
          />
        </div>

        <button type="submit" style={{ padding: '10px 20px' }}>Submit</button>
      </form>
    </div>
  );
}